  <article class="filterDiv realitevirtuelle image">
    <h3>
      CADOZ, Claude (auteur).  
    </h3>

    <h4>
      <a href="https://www.decitre.fr/ebooks/les-realites-virtuelles-9782403027860_9782403027860_1.html#" target="_blank">
        Les réalités virtuelles 
      </a>
    </h4> 
    
    <h5>
      Flammarion, publié en 1994.
    </h5>
    
    <blockquote cit="#">L'image aujourd'hui n'est plus seulement cet objet plan fait uniquement pour les yeux&#8239;;
    ce sont des objets que l'on peut toucher, manipuler,entendre, et qui résistent ou s'animent sous nos mains, c'est un espace dans lequel on peut entrer.</blockquote>
    
    <p> 
    Ce livre décrit les techniques et le effets des réalités virtuelles et montre comment ces dernières permettent une immersion dans l'image.
    </p>
   

    <h6> 
    lu 08 Octobre 2021
    </h6>
  
  </article>

